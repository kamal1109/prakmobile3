package com.example.recycleview;

public class Toko_Gaya {
    private String namatoko;
    private int lambangtoko;
    private String privew;

    public String getNamatoko() {
        return namatoko;
    }

    public void setNamatoko(String namatoko) {
        this.namatoko = namatoko;
    }

    public int getLambangtoko() {
        return lambangtoko;
    }

    public void setLambangtoko(int lambangtoko) {
        this.lambangtoko = lambangtoko;
    }

    public String getPrivew() {
        return privew;
    }

    public void setPrivew(String privew) {
        this.privew = privew;
    }
}
